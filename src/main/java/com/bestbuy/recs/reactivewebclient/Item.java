package com.bestbuy.recs.reactivewebclient;

public class Item {

    private String sku;

    private double score;

    public String getSku() {
        return sku;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }
}
